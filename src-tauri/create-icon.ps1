# 创建一个简单的占位图标文件
# 这个脚本会创建一个最小的 icon.ico 文件

$iconPath = "icons\icon.ico"

# 确保 icons 目录存在
if (-not (Test-Path "icons")) {
    New-Item -ItemType Directory -Path "icons" | Out-Null
}

# 创建一个最小的 ICO 文件（16x16 单色）
# 注意：这是一个非常简单的占位图标
$bytes = @(
    0x00, 0x00, 0x01, 0x00, 0x01, 0x00, 0x10, 0x10, 0x00, 0x00, 0x01, 0x00,
    0x20, 0x00, 0x68, 0x04, 0x00, 0x00, 0x16, 0x00, 0x00, 0x00, 0x28, 0x00,
    0x00, 0x00, 0x10, 0x00, 0x00, 0x00, 0x20, 0x00, 0x00, 0x00, 0x01, 0x00,
    0x20, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x04, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    0x00, 0x00
)

# 填充剩余的字节（创建一个简单的蓝色方块）
for ($i = 0; $i -lt 1024; $i++) {
    $bytes += 0x00, 0x7F, 0xFF, 0xFF  # ARGB: 半透明蓝色
}

[System.IO.File]::WriteAllBytes((Resolve-Path ".").Path + "\" + $iconPath, $bytes)
Write-Host "已创建占位图标: $iconPath"

